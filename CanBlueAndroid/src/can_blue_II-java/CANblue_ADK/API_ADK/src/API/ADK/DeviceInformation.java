package API.ADK;

/**
 * Structure for the Device information, such as firmware version,
 * protocol version, and hardware number.
 * @author hroesdiyono
 */
public class DeviceInformation
{
  /** The string contains firmware version. */
  public String firmwareVersion;
  /** The string contains protocol version. */
  public String protocolVersion;
  /** The string contains hardware number. */
  public String hardwareNumber;

  /** The default constructor of the DeviceInformation. */
  public DeviceInformation()
  {
    // Do nothing;
  }
}